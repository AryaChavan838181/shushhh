"""
shushhh key server — .onion identity and key distribution

Endpoints:
    POST /register          — Create a new user account
    POST /auth              — Authenticate and return Ed25519-signed token
    POST /upload_key        — Upload public keys for a username (requires auth signature)
    GET  /get_key/<user>    — Retrieve public keys for a specific username

This server holds usernames, password hashes, and public keys. It does NOT hold
any messages or private keys.
"""

import os
import sys
import time
import hmac
import hashlib
import sqlite3
import base64
import json
import uuid
from functools import wraps

from flask import Flask, request, jsonify, g
import nacl.signing
import nacl.encoding

app = Flask(__name__)

# ============================================================
# Configuration
# ============================================================

DATABASE = os.environ.get("SHUSHHH_KEYDB", "keyserver.db")
KEY_FILE = os.path.join(os.path.dirname(os.path.abspath(__file__)), "server_key.bin")

# ============================================================
# Ed25519 server signing key
# ============================================================

_signing_key = None

def load_signing_key():
    """Load the Ed25519 private key generated by setup_keys.py."""
    global _signing_key
    if not os.path.exists(KEY_FILE):
        print(f"[-] Server key not found at {KEY_FILE}")
        print("    Run setup_keys.py first to generate the keypair")
        sys.exit(1)
    with open(KEY_FILE, "rb") as f:
        key_bytes = f.read()
    _signing_key = nacl.signing.SigningKey(key_bytes)
    pub_hex = _signing_key.verify_key.encode(nacl.encoding.HexEncoder).decode()
    print(f"[+] Key Server Ed25519 public key: {pub_hex}")

def sign_payload(payload: str) -> str:
    """Sign a payload string with the server's Ed25519 key. Returns base64 signature."""
    sig = _signing_key.sign(payload.encode(), encoder=nacl.encoding.RawEncoder)
    return base64.b64encode(sig.signature).decode()

def verify_payload(payload: str, signature_b64: str) -> bool:
    """Verify an auth signature provided by the client (which we previously signed)."""
    try:
        sig_bytes = base64.b64decode(signature_b64)
        _signing_key.verify_key.verify(payload.encode(), sig_bytes)
        return True
    except Exception:
        return False

# ============================================================
# Database
# ============================================================

def get_db():
    if "db" not in g:
        g.db = sqlite3.connect(DATABASE)
        g.db.row_factory = sqlite3.Row
    return g.db

@app.teardown_appcontext
def close_db(exception):
    db = g.pop("db", None)
    if db is not None:
        db.close()

def init_db():
    db = sqlite3.connect(DATABASE)

    # User credentials table
    db.execute("""
        CREATE TABLE IF NOT EXISTS app_config (
            config_key    TEXT PRIMARY KEY,
            config_salt   TEXT NOT NULL,
            config_value  TEXT NOT NULL
        )
    """)

    # User public keys table
    db.execute("""
        CREATE TABLE IF NOT EXISTS user_keys (
            username      TEXT PRIMARY KEY,
            public_keys   TEXT NOT NULL,
            updated_at    INTEGER NOT NULL
        )
    """)

    db.commit()
    db.close()
    print("[+] Database initialized")

