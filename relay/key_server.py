# Endpoints
# ============================================================

@app.route("/register", methods=["POST"])
def register():
    """
    Register a new user account.
    Expects: { "username": "...", "credential_hash": "base64..." }
    """
    data = request.get_json(silent=True)
    if not data or "username" not in data or "credential_hash" not in data:
        return jsonify({"status": "error", "message": "missing fields"}), 400

    username = data["username"]
    cred_hash_b64 = data["credential_hash"]

    db = get_db()
    existing = db.execute("SELECT 1 FROM app_config WHERE config_key = ?", (username,)).fetchone()
    if existing:
        return jsonify({"status": "error", "message": "username already taken"}), 409

    salt = os.urandom(32)
    salt_hex = salt.hex()

    try:
        cred_hash = base64.b64decode(cred_hash_b64)
    except Exception:
        return jsonify({"status": "error", "message": "invalid credential hash"}), 400

    salted = hashlib.sha256(cred_hash + salt).hexdigest()

    db.execute(
        "INSERT INTO app_config (config_key, config_salt, config_value) VALUES (?, ?, ?)",
        (username, salt_hex, salted)
    )
    db.commit()

    return jsonify({"status": "ok", "message": "registered"})

@app.route("/auth", methods=["POST"])
def auth():
    """
    Authenticate a user.
    Expects: { "username": "...", "credential_hash": "base64..." }
    Returns: { "status": "ok", "payload": "...", "signature": "base64..." }
    """
    data = request.get_json(silent=True)
    if not data or "username" not in data or "credential_hash" not in data:
        return jsonify({"status": "error", "message": "missing fields"}), 400

    username = data["username"]
    cred_hash_b64 = data["credential_hash"]

    db = get_db()
    row = db.execute("SELECT config_salt, config_value FROM app_config WHERE config_key = ?", (username,)).fetchone()

    if not row:
        return jsonify({"status": "error", "message": "authentication failed"}), 401

    try:
        cred_hash = base64.b64decode(cred_hash_b64)
    except Exception:
        return jsonify({"status": "error", "message": "invalid credential"}), 400

    salt = bytes.fromhex(row["config_salt"])
    computed = hashlib.sha256(cred_hash + salt).hexdigest()

    if not hmac.compare_digest(computed, row["config_value"]):
        return jsonify({"status": "error", "message": "authentication failed"}), 401

    # Issue token (signed payload)
    payload = json.dumps({
        "username": username,
        "timestamp": int(time.time()),
        "nonce": uuid.uuid4().hex
    })
    signature = sign_payload(payload)

    return jsonify({
        "status": "ok",
        "payload": payload,
        "signature": signature
    })

@app.route("/upload_key", methods=["POST"])
def upload_key():
    """
    Upload public keys for a user.
    Expects: { "payload": "...", "signature": "...", "public_keys": "json string of keys" }
    """
    data = request.get_json(silent=True)
    if not data or "payload" not in data or "signature" not in data or "public_keys" not in data:
        return jsonify({"status": "error", "message": "missing auth or keys"}), 400

    payload = data["payload"]
    signature = data["signature"]
    public_keys = data["public_keys"]

    if not verify_payload(payload, signature):
        return jsonify({"status": "error", "message": "invalid auth signature"}), 401

    try:
        parsed_payload = json.loads(payload)
        username = parsed_payload["username"]
        # Check token expiration (e.g., 24 hours)
        if time.time() - parsed_payload["timestamp"] > 86400:
            return jsonify({"status": "error", "message": "token expired"}), 401
    except Exception:
        return jsonify({"status": "error", "message": "invalid payload"}), 400

    db = get_db()
    db.execute(
        "INSERT OR REPLACE INTO user_keys (username, public_keys, updated_at) VALUES (?, ?, ?)",
        (username, public_keys, int(time.time()))
    )
    db.commit()

    return jsonify({"status": "ok", "message": "keys uploaded"})

@app.route("/get_key/<username>", methods=["GET"])
def get_key(username):
    """
    Retrieve public keys for a specific username.
    """
    db = get_db()
    row = db.execute("SELECT public_keys FROM user_keys WHERE username = ?", (username,)).fetchone()

    if not row:
        return jsonify({"status": "error", "message": "keys not found"}), 404

    return jsonify({"status": "ok", "public_keys": row["public_keys"]})


