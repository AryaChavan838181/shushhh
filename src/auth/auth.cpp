std::string tor_post(const std::string& url, const std::string& json_payload) {
    CURL* curl = curl_easy_init();
    if (!curl) {
        std::cerr << "[-] curl_easy_init() failed\n";
        return "";
    }

    std::string response;

    // Only route through Tor if it's an onion service
    if (url.find(".onion") != std::string::npos) {
        curl_easy_setopt(curl, CURLOPT_PROXY, "socks5h://127.0.0.1:9050");
    }
    
    curl_easy_setopt(curl, CURLOPT_URL, url.c_str());
    curl_easy_setopt(curl, CURLOPT_POSTFIELDS, json_payload.c_str());
    curl_easy_setopt(curl, CURLOPT_WRITEFUNCTION, write_cb);
    curl_easy_setopt(curl, CURLOPT_WRITEDATA, &response);
    curl_easy_setopt(curl, CURLOPT_TIMEOUT, 30L);

    struct curl_slist* headers = nullptr;
    headers = curl_slist_append(headers, "Content-Type: application/json");
    curl_easy_setopt(curl, CURLOPT_HTTPHEADER, headers);

    CURLcode res = curl_easy_perform(curl);
    curl_slist_free_all(headers);
    curl_easy_cleanup(curl);

    if (res != CURLE_OK) {
        std::cerr << "[-] Tor POST failed: " << curl_easy_strerror(res) << "\n";
        return "";
    }

    return response;
}

std::string tor_get(const std::string& url) {
    CURL* curl = curl_easy_init();
    if (!curl) {
        std::cerr << "[-] curl_easy_init() failed\n";
        return "";
    }

    std::string response;

    // Only route through Tor if it's an onion service
    if (url.find(".onion") != std::string::npos) {
        curl_easy_setopt(curl, CURLOPT_PROXY, "socks5h://127.0.0.1:9050");
    }

    curl_easy_setopt(curl, CURLOPT_URL, url.c_str());
    curl_easy_setopt(curl, CURLOPT_WRITEFUNCTION, write_cb);
    curl_easy_setopt(curl, CURLOPT_WRITEDATA, &response);
    curl_easy_setopt(curl, CURLOPT_TIMEOUT, 30L);

    CURLcode res = curl_easy_perform(curl);
    curl_easy_cleanup(curl);

    if (res != CURLE_OK) {
        std::cerr << "[-] Tor GET failed: " << curl_easy_strerror(res) << "\n";
        return "";
    }

    return response;
}

// ============================================================
// Platform-specific password input (no echo)
// ============================================================

static std::string read_password() {
    std::string password;

#ifdef _WIN32
    // Windows: use ReadConsole with echo disabled
    HANDLE hStdin = GetStdHandle(STD_INPUT_HANDLE);
    DWORD mode;
    GetConsoleMode(hStdin, &mode);
    SetConsoleMode(hStdin, mode & ~ENABLE_ECHO_INPUT);

    std::getline(std::cin, password);

    SetConsoleMode(hStdin, mode); // restore
    std::cout << "\n"; // newline since echo was off
#else
    // POSIX: toggle termios echo
    struct termios oldt, newt;
    tcgetattr(STDIN_FILENO, &oldt);
    newt = oldt;
    newt.c_lflag &= ~ECHO;
    tcsetattr(STDIN_FILENO, TCSANOW, &newt);

    std::getline(std::cin, password);

    tcsetattr(STDIN_FILENO, TCSANOW, &oldt);
    std::cout << "\n";
#endif

    return password;
}

