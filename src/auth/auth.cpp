bool PasswordLogin::upload_public_keys(const std::string& server_url, const std::string& public_keys_json) {
    if (!authenticated_ || auth_token_.empty() || auth_signature_.empty()) {
        std::cerr << "[-] Cannot upload keys: not authenticated\n";
        return false;
    }

    json upload_payload;
    upload_payload["payload"] = auth_token_;
    upload_payload["signature"] = auth_signature_;
    upload_payload["public_keys"] = public_keys_json;

    std::string url = server_url + "/upload_key";
    std::cout << "[*] Uploading public keys to Key Server...\n";

    std::string response = tor_post(url, upload_payload.dump());
    if (response.empty()) return false;

    try {
        json resp = json::parse(response);
        if (resp.contains("status") && resp["status"] == "ok") {
            std::cout << "[+] Public keys successfully published to Key Server\n";
            return true;
        } else {
            std::cerr << "[-] Key upload failed: " << resp.value("message", "unknown") << "\n";
            return false;
        }
    } catch (...) {
        return false;
    }
}

std::string PasswordLogin::fetch_public_keys(const std::string& server_url, const std::string& target_username) {
    std::string url = server_url + "/get_key/" + target_username;
    std::string response = tor_get(url);
    if (response.empty()) return "";

    try {
        json resp = json::parse(response);
        if (resp.contains("status") && resp["status"] == "ok") {
            return resp["public_keys"].get<std::string>();
        }
    } catch (...) {}
    return "";
}

// Utility for hex
static std::string bin_to_hex(const unsigned char* data, size_t len) {
    std::string hex_chars = "0123456789abcdef";
    std::string out;
    for (size_t i = 0; i < len; ++i) {
        out += hex_chars[data[i] >> 4];
        out += hex_chars[data[i] & 0x0f];
    }
    return out;
}

static bool hex_to_bin(const std::string& hex, std::vector<unsigned char>& out) {
    if (hex.size() % 2 != 0) return false;
    out.resize(hex.size() / 2);
    for (size_t i = 0; i < out.size(); ++i) {
        unsigned int byte;
        std::istringstream iss(hex.substr(i * 2, 2));
        iss >> std::hex >> byte;
        if (iss.fail()) return false;
        out[i] = static_cast<unsigned char>(byte);
    }
    return true;
}

bool PasswordLogin::save_identity(const HybridKeyPair& keypair, const std::string& filepath) {
    if (credential_hash_.empty()) return false;

    // Serialize keypair to JSON
    json j;
    j["x25519_private"] = bin_to_hex(keypair.x25519_private, 32);
    j["x25519_public"] = bin_to_hex(keypair.x25519_public, 32);
    j["kyber_private"] = bin_to_hex(keypair.kyber_private.data(), keypair.kyber_private.size());
    j["kyber_public"] = bin_to_hex(keypair.kyber_public.data(), keypair.kyber_public.size());
    
    std::string plaintext = j.dump();

    // Encrypt using credential hash as the symmetric key
    EncryptedMessage enc = encrypt_message(plaintext, credential_hash_.data(), 8192);
    std::string encrypted_json = encrypted_to_json(enc);

    std::ofstream out(filepath, std::ios::binary);
    if (!out) return false;
    out << encrypted_json;
    return true;
}

bool PasswordLogin::load_identity(HybridKeyPair& keypair, const std::string& filepath) {
    if (credential_hash_.empty()) return false;

    std::ifstream in(filepath, std::ios::binary);
    if (!in) return false; // File doesn't exist
    std::stringstream buffer;
    buffer << in.rdbuf();

    EncryptedMessage enc = json_to_encrypted(buffer.str());
    if (enc.ciphertext.empty()) return false;

    std::string plaintext = decrypt_message(enc, credential_hash_.data());
    if (plaintext.empty()) return false; // Wrong password

    try {
        json j = json::parse(plaintext);
        
        std::vector<unsigned char> temp;
        if (!hex_to_bin(j["x25519_private"].get<std::string>(), temp) || temp.size() != 32) return false;
        std::memcpy(keypair.x25519_private, temp.data(), 32);

        if (!hex_to_bin(j["x25519_public"].get<std::string>(), temp) || temp.size() != 32) return false;
        std::memcpy(keypair.x25519_public, temp.data(), 32);

        if (!hex_to_bin(j["kyber_private"].get<std::string>(), keypair.kyber_private)) return false;
        if (!hex_to_bin(j["kyber_public"].get<std::string>(), keypair.kyber_public)) return false;
        
        return true;
    } catch (...) {
        return false;
    }
}

