void PasswordLogin::prompt_credentials() {
    std::cout << "Username: ";
    std::getline(std::cin, username_);

    std::cout << "Password: ";
    std::string password = read_password();

    // Compute SHA-256(password + username) — raw password never leaves device
    std::string combined = password + username_;
    credential_hash_ = sha256_hash(combined);

    // Wipe the raw password from memory immediately
    secure_wipe(&password[0], password.size());
    secure_wipe(&combined[0], combined.size());
}

std::vector<unsigned char> PasswordLogin::get_credential_hash() {
    return credential_hash_;
}

std::string PasswordLogin::get_username() const {
    return username_;
}

bool PasswordLogin::authenticate(const std::string& server_url) {
    prompt_credentials();

    // Build auth payload
    json auth_payload;
    auth_payload["username"] = username_;

    // Base64-encode the credential hash for transport
    size_t b64_maxlen = sodium_base64_ENCODED_LEN(
        credential_hash_.size(), sodium_base64_VARIANT_ORIGINAL);
    std::string hash_b64(b64_maxlen, '\0');
    sodium_bin2base64(&hash_b64[0], b64_maxlen,
                      credential_hash_.data(), credential_hash_.size(),
                      sodium_base64_VARIANT_ORIGINAL);
    hash_b64.resize(strlen(hash_b64.c_str()));

    auth_payload["credential_hash"] = hash_b64;

    std::string url = server_url + "/auth";
    std::cout << "[*] Authenticating with server...\n";

    std::string response = tor_post(url, auth_payload.dump());
    if (response.empty()) {
        std::cerr << "[-] Authentication failed — no response from server\n";
        return false;
    }

    // Parse server response
    try {
        json resp = json::parse(response);

        if (!resp.contains("status") || resp["status"] != "ok") {
            std::cerr << "[-] Authentication failed: "
                      << resp.value("message", "unknown error") << "\n";
            return false;
        }

        // Verify Ed25519 signature on the response
        if (resp.contains("signature") && resp.contains("payload")) {
            std::string sig_b64 = resp["signature"].get<std::string>();
            std::string payload = resp["payload"].get<std::string>();

            // Decode the signature
            std::vector<unsigned char> sig(sig_b64.size());
            size_t sig_len = 0;
            if (sodium_base642bin(sig.data(), sig.size(),
                                  sig_b64.c_str(), sig_b64.size(),
                                  nullptr, &sig_len, nullptr,
                                  sodium_base64_VARIANT_ORIGINAL) != 0) {
                std::cerr << "[-] Failed to decode server signature\n";
                return false;
            }
            sig.resize(sig_len);

            // Verify against the hardcoded server public key
            bool valid = ed25519_verify_detached(
                sig.data(),
                reinterpret_cast<const unsigned char*>(payload.data()),
                payload.size(),
                SERVER_ED25519_PUBKEY
            );

            if (!valid) {
                std::cerr << "[-] CRITICAL: Server signature verification FAILED\n";
                std::cerr << "    Possible MITM attack — aborting authentication\n";
                return false;
            }
            std::cout << "[+] Server Ed25519 signature verified\n";

            auth_token_ = payload;
            auth_signature_ = sig_b64;
        }

        authenticated_ = true;
        std::cout << "[+] Authentication successful\n";
        return true;

    } catch (const json::exception& e) {
        std::cerr << "[-] Failed to parse auth response: " << e.what() << "\n";
        return false;
    }
}

